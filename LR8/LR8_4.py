num1 = "123456789012345678901234567890"
num2 = "987654321098765432109876543210"

# Доповніть коротше число нулями зліва, щоб вони мали однакову довжину
num2 = num2.rjust(len(num1), '0')

result = []  # Для зберігання результату
carry = 0    # Для відстеження переносу

# Виконайте додавання по розрядам справа наліво
for i in range(len(num1) - 1, -1, -1):
    digit1 = int(num1[i])
    digit2 = int(num2[i])

    # Додайте поточні розряди та перенос
    temp_sum = digit1 + digit2 + carry

    # Додайте поточний розряд до результату (залишок від temp_sum)
    result.append(str(temp_sum % 10))

    # Обчисліть перенос для наступного розряду
    carry = temp_sum // 10

# Якщо є залишковий перенос, додайте його до результату
if carry:
    result.append(str(carry))

# Результат - це перевернутий список, приєднаний у рядок
sum_result = ''.join(result[::-1])

# Виведення суми
print(f"Сума: {sum_result}")
